my_list = [1, 2, 3, 4, 5]
print(my_list[2:4])  #items from one position to another position
print(my_list[::2])  #items at Specified intervals
print(my_list[::-1]) #items in reverse order
print(my_list[-3:])  #items from the end of the list
print(my_list[:-3])  #items from the beginning of the list
print(my_list[-3:-1]) #items from the end of the list to the specified position
